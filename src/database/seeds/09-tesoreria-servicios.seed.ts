export async function seedTesoreriaServicios(app?: any) {
  const shouldClose = !app;
  if (!app) {
    app = await NestFactory.createApplicationContext(AppModule);
  }

  try {
    const servicioModel = app.get(
      getModelToken(ServicioCobro.name),
    ) as Model<ServicioCobro>;

    console.log(
      '🏦 [09] Seeding Catálogo de Servicios y Trámites Municipales...\n',
    );

    let insertados = 0;
    let actualizados = 0;
    let sinCambios = 0;

    const categoriaStats: Record<string, number> = {};

    for (const servicio of SERVICIOS) {
      const result = await servicioModel.updateOne(
        { clave: servicio.clave },
        {
          $set: {
            nombre: servicio.nombre,
            descripcion: servicio.descripcion,
            categoria: servicio.categoria,
            areaResponsable: servicio.areaResponsable,
            costo: servicio.costo,
            montoVariable: servicio.montoVariable,
            requiereContribuyente: servicio.requiereContribuyente,
            orden: servicio.orden,
            activo: true,
          },
          $setOnInsert: {
            municipioId: null,
          },
        },
        { upsert: true },
      );

      categoriaStats[servicio.categoria] =
        (categoriaStats[servicio.categoria] || 0) + 1;

      if (result.upsertedCount > 0) {
        insertados++;
        console.log(`   ✅ CREADO: [${servicio.categoria}] ${servicio.nombre}`);
      } else if (result.modifiedCount > 0) {
        actualizados++;
        console.log(`   🔄 ACTUALIZADO: ${servicio.nombre}`);
      } else {
        sinCambios++;
        console.log(`   ⏭️  SIN CAMBIOS: ${servicio.nombre}`);
      }
    }

    console.log('\n' + '='.repeat(70));
    console.log('✅ Trámites y servicios municipales configurados:');
    console.log(`   ✅ Insertados : ${insertados}`);
    console.log(`   🔄 Actualizados: ${actualizados}`);
    console.log(`   ⏭️  Sin cambios : ${sinCambios}`);
    console.log(`   📦 Total       : ${SERVICIOS.length}`);
    console.log('\n📊 Por categoría:');
    Object.entries(categoriaStats)
      .sort(([, a], [, b]) => b - a)
      .forEach(([cat, count]) => {
        console.log(`   • ${cat}: ${count}`);
      });
    console.log('='.repeat(70) + '\n');

    return { insertados, actualizados, sinCambios, total: SERVICIOS.length };
  } catch (error) {
    console.error('❌ Error en seed de servicios municipales:', error);
    throw error;
  } finally {
    if (shouldClose) {
      await app.close();
    }
  }
}

// Permitir ejecución directa
if (require.main === module) {
  seedTesoreriaServicios();
}
